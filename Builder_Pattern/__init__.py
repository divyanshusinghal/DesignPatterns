# Motivation for Builder Design Pattern

# Some objects are simple and can be created in a single initializer call
#
# Other object require a lot of ceremony to create
#
# Having an object with 10 initializer arguements is not productive
#
# instead opt for piecewise construction
#
# Builder provides an API for constructing an object step-by-step

# FORMAL DEFINITION

# When piecesise object  construction is complicated, Builder provides an API for doing it
